// Generates src/data/schedule.generated.ts from the real Discover/FRA schedule CSV.
//
// The CSV is the schedule + sizing only (no risk/financials). This script cleans it
// (drops quality_flag="review" and distance<=0), parses days_of_week and duration,
// dedupes by record_id, and emits a typed, statically-importable module.
//
// Run: pnpm gen:schedule  (re-run whenever assets/SIZED_FRA_DISC_DATA.csv changes)

import { readFileSync, writeFileSync, mkdirSync } from "node:fs";
import { fileURLToPath } from "node:url";
import { dirname, resolve } from "node:path";

const here = dirname(fileURLToPath(import.meta.url));
const CSV_PATH = resolve(here, "../../assets/SIZED_FRA_DISC_DATA.csv");
const OUT_PATH = resolve(here, "../src/data/schedule.generated.ts");

// Minimal quote-aware CSV line parser (handles commas inside quoted fields).
function parseLine(line) {
  const out = [];
  let field = "";
  let inQuotes = false;
  for (let i = 0; i < line.length; i++) {
    const c = line[i];
    if (inQuotes) {
      if (c === '"') inQuotes = false;
      else field += c;
    } else if (c === '"') {
      inQuotes = true;
    } else if (c === ",") {
      out.push(field);
      field = "";
    } else {
      field += c;
    }
  }
  out.push(field);
  return out;
}

function parseDuration(text) {
  const m = /(?:(\d+)h)?(?:(\d+)m)?/.exec(text.trim());
  const h = m && m[1] ? Number(m[1]) : 0;
  const min = m && m[2] ? Number(m[2]) : 0;
  return h * 60 + min;
}

const raw = readFileSync(CSV_PATH, "utf8").trim();
const [headerLine, ...lines] = raw.split(/\r?\n/);
const headers = parseLine(headerLine);
const col = (row, name) => row[headers.indexOf(name)];

const seen = new Set();
const flights = [];

for (const line of lines) {
  if (!line.trim()) continue;
  const row = parseLine(line);

  const quality = col(row, "quality_flag");
  const distanceKm = Number(col(row, "distance_km"));
  if (quality === "review") continue; // drop flagged-for-review rows
  if (!Number.isFinite(distanceKm) || distanceKm <= 0) continue; // drop broken legs

  const recordId = col(row, "record_id");
  if (seen.has(recordId)) continue; // dedupe repeated departures
  seen.add(recordId);

  const flightNo = col(row, "flight_number");
  flights.push({
    recordId,
    flightNo,
    code: flightNo.replace(/^4Y/, "4Y "),
    route: col(row, "route"),
    destination: col(row, "destination_iata"),
    destinationName: col(row, "destination_airport"),
    days: col(row, "days_of_week").split(";").map((d) => d.trim()).filter(Boolean),
    departure: col(row, "departure_time_local"),
    arrival: col(row, "arrival_time_local"),
    durationMin: parseDuration(col(row, "duration")),
    distanceKm,
    size: col(row, "Size"),
    pax: Number(col(row, "Cust_num")),
  });
}

// Stable order: by departure time, then code.
flights.sort((a, b) => a.departure.localeCompare(b.departure) || a.code.localeCompare(b.code));

const banner =
  "// AUTO-GENERATED by scripts/gen-schedule.mjs from assets/SIZED_FRA_DISC_DATA.csv.\n" +
  "// Do not edit by hand — run `pnpm gen:schedule` to regenerate.\n\n";

const types =
  'export type Weekday =\n' +
  '  | "Monday"\n  | "Tuesday"\n  | "Wednesday"\n  | "Thursday"\n  | "Friday"\n  | "Saturday"\n  | "Sunday";\n\n' +
  "export type AircraftSize = \"S\" | \"M\" | \"L\";\n\n" +
  "export type ScheduleFlight = {\n" +
  "  recordId: string;\n" +
  "  flightNo: string;\n" +
  "  code: string;\n" +
  "  route: string;\n" +
  "  destination: string;\n" +
  "  destinationName: string;\n" +
  "  days: Weekday[];\n" +
  "  departure: string;\n" +
  "  arrival: string;\n" +
  "  durationMin: number;\n" +
  "  distanceKm: number;\n" +
  "  size: AircraftSize;\n" +
  "  pax: number;\n" +
  "};\n\n";

const body =
  "export const schedule: ScheduleFlight[] = [\n" +
  flights.map((f) => "  " + JSON.stringify(f) + ",").join("\n") +
  "\n];\n";

mkdirSync(dirname(OUT_PATH), { recursive: true });
writeFileSync(OUT_PATH, banner + types + body);

console.log(`Wrote ${flights.length} flights to ${OUT_PATH}`);
